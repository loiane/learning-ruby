# http://www.cs.mun.ca/~donald/slug/2003-10-16/presentation/img5.html

# The increment and decrement operators (++ and --) are not available
# in Ruby, neither in "pre" nor "post" forms. However, do note that the +=
# and -= are available
puts (5 % 3) # prints 2
puts (-5 % 3) # prints 1
puts (5 % -3) # prints -1
puts (-5 % -3) # prints -2



